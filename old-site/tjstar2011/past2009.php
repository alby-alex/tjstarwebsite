<h1>Past Symposium - 2009</h1>
<p>
The first annual TJSTAR event included presentations by a variety of scientific professionals and prominent members of society, most notably the U.S. Secretary of Education, Arne Duncan. The U.S. Army also participated, along with representatives from NASA, Lockheed Martin, Sun Microsystems, and the U.S. Air Force. This year it also included speakers and panels on the topics of women in science, science-related public policy, and global warming.
