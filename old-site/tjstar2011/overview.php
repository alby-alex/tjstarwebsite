<h2>Speakers</h2>
<p>
Professionals from all areas of science and technology are welcome to come and share their knowledge through interactive presentations and discussions. Speakers can also be accompanied by a team of specialists.  
</p>

<h2>Panels</h2>
<p>Panels are interactive sessions involving student discussion on issues ranging from neuroethics to global warming. Panelists will provide students with a brief overview of their experience in the panel's topic, and after these introductions, students are invited to address a question to any or all of the panelists relating to the panel topic.</p>
<p>
We will coordinate communication between panelists prior to the symposium in the hope that panelists will be able to prepare for the event with the group in mind. We encourage panelists to reach out to others in their respective fields both to gather opinions and invite others to participate in TJSTAR. 
</p>

<h2>Workshops</h2>
<p>
Topical, interactive presentations on original research by TJ students, TJ faculty, or invited guests. We invite you to provide instructive workshops for our students to learn new skills.
</p>

<h2>Displays and Demonstrations</h2>
<p>
Each guest involved with the Displays and Demonstration category will be situated at individual booths in a large area of the school, allowing for students to visit each booth at their leisure. Guests wishing to be part of the Displays and Demonstrations are encouraged to bring interactive media and hands-on activities that high school students with technological interests would enjoy.
</p>

<h2>Design Challenges</h2>
<p>
<img src="images/designchallenge.jpg" alt="" style="float:right;padding: 5px; padding-left:20px;">
Teams of TJ students will be in competition to solve fun, real-world engineering problems. We encourage you to create a science or technology related challenge and provide materials or supplies for teams of three or four students to solve within an hour or so.
</p>

<h2>Senior Tech Projects</h2>
<p>
Senior Tech Lab Projects are year-long projects that all Seniors must complete in a research lab of their choosing. Labs range from the Chemical Analysis Lab and the Neuroscience Lab to the Energy Systems Lab and the Computer Systems Lab. Senior students will be presenting the results of their research during the symposium. Find out more about the research labs at the <a href="http://www.tjhsst.edu/discovery/labs/">main TJ website</a>. 
</p>

<h2>IBET Projects</h2>
<p>
During the IBET presentations, groups of freshmen students will present year-long research projects they have developed in their IBET (integrated biology, English, and technology) classes. The experiments they have completed will address various aspects of biology ranging from environmental protection to extensive research on specific microorganisms. Each project will also incorporate a student-designed technology component. 
</p>

