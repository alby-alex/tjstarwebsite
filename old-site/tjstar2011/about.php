<h1>About tjSTAR</h1>

<h2>Introduction</h2>
<p>
The Thomas Jefferson Symposium To Advance Research (TJSTAR), an annual event hosted by the Thomas Jefferson High School for Science and Technology, celebrates cutting-edge innovations in all fields of scientific development. It plays host to a wide range of panels and demonstrations, featuring professional scientists and policy-makers. TJSTAR has attracted the attention of the scientific community with its debut in 2009, which was extremely well received. 
</p>
<h2>Past Highlights</h2>
<p>
The first annual TJSTAR event included presentations by a variety of scientific professionals and prominent members of society, most notably the U.S. Secretary of Education, Arne Duncan. The U.S. Army also participated, along with representatives from NASA, Lockheed Martin, Sun Microsystems, and the U.S. Air Force. TJSTAR is not limited to only specific scientific innovations, however; last year it also included speakers and panels on the topics of women in science, science-related public policy, and global warming.
</p>
<h2>Student Involvement</h2>
<p>
Not only does TJSTAR offer a myriad of professional exhibitions, but also features presentations of student-conducted research. Thomas Jefferson has been hailed as the foremost magnet school in the nation (<i>U.S. News and World Report</i>); in addition, its rigorous science program allows students to conduct highly individualized research projects in a variety of fields, including biotechnology, neuroscience, and robotics. With thirteen separate research laboratories and numerous opportunities for extramural research, Jefferson offers its students the resources they need to conduct high-grade research and experimentation. TJSTAR 2009 spotlighted student research in solar panel and biodiesel technology and a neural-directed wheelchair, among various other student projects. 
</p>
